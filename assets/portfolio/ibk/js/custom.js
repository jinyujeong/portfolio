$(document).ready(function(){
    //배너 슬라이드
    $('#banner-slide').slick({
        arrows:true,
        autoplay:true,
        autoplaySpeed:5000
    });
    $('.slb_slide').slick({
        arrows:true
    });
    $('.sbr_slide').slick({
      dots:true,
      arrows:false,
      autoplay:true,
      autoplaySpeed:6000
    });
    $('.sbn_slide').slick({
      dots:true,
      arrows:false,
      autoplay:true,
      autoplaySpeed:6000
    });
    //전체메뉴
    $('.all_menu').click(function(e){
      e.preventDefault();
      $('#site').slideToggle(400);
    })
    //뱅킹로그인 메뉴
    $('.bklogin').hover(function(e){
      e.preventDefault();
      $('.bklogin_menu').slideToggle(400);
    })
  });